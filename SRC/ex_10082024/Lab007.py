# Identifer

# the name we give to identify a variables to distinguish one entity from another entity

# we cannot start an identifier with a number
# 23dfg=76 Invalid Identifier

# We cannot use reserved keywords
# Print,max,True etc Invalid Identifier

# abc@ Invalid Identifier

# They start with a letter (A-Z or a-z)
abc = 10
print(abc)
# an underscore (_) followed by zero
_0 = 34
print(_0)

# no restriction on the length
no_restriction_on_the_length = 123
print(no_restriction_on_the_length)

# We can use ‘_’ as a special character in the naming of a variable
_ = 55
_ = _ + 98
print(_)

lab007_ = 44
print(lab007_)

a_b = 56
print(a_b)

_python = "Funny"
print(_python)

